            'name' => 'required',
            'email' => 'required',
            'subject' => 'required',
            'message' => 'required',
        ]);
        log::info('yaha tak to aa gaya h');
        $contactdata = new Contact;
        $contactdata->name = $request->name;
        $contactdata->email = $request->email;
        $contactdata->subject = $request->subject;
        $contactdata->message = $request->message;
        
        log::info('data mil gaya');
        $contactdata->save();
        log::info($contactdata);
        return response()->json([
            'status'=> 200,
            'message'=>'Sent Successfully',

